const getRandomName = () => {
  const model = models[Math.floor(Math.random() * models.length)];
  const name = names[Math.floor(Math.random() * models.length)];
  return `${name} ${model}`;
};
const getRandomColor = () => {
  const symbols = '0123456789ABCDEF';
  let color = '#';
  for (let i = 0; i < 6; i++) {
    color += symbols[Math.floor(Math.random() * 16)];
  }
  return color;
};

export const generateRandomCars = (countCars = 100): ICreateCar[] =>
  new Array(countCars).fill(0).map(() => ({ name: getRandomName(), color: getRandomColor() }));
